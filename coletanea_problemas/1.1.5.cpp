# include <bits/stdc++.h>
using namespace std;

int main() {
    numeric_limits<int> it;
    cout << "min: " << it.min() << endl;
    cout << "max: " << it.max() << endl;

    numeric_limits<unsigned int> uit;
    cout << "min: " << uit.min() << endl;
    cout << "max: " << uit.max() << endl;

    numeric_limits<float> ft;
    cout << "min: " << ft.min() << endl;
    cout << "max: " << ft.max() << endl;

    numeric_limits<double> dt;  
    cout << "min: " << dt.min() << endl;
    cout << "max: " << dt.max() << endl;  
}